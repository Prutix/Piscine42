#include <unistd.h>

int	main(int argc, char *argv[])
{
	int		i;
	int		a;
	char	c;

	i = argc - 1;
	a = 0;
	while (i >= 1)
	{
		a = 0;
		while (argv[i][a])
		{
			c = argv[i][a];
			write(1, &c, 1);
			a++;
		}
		write(1, "\n", 1);
		i--;
	}
}
