void	ft_rev_int_tab(int *tab, int size)
{
	int	i;
	int	a;
	int	b;

	i = 0;
	a = size - 1;
	while (i < (size / 2))
	{
		b = tab[i];
		tab[i] = tab[a];
		tab[a] = b;
		a--;
		i++;
	}
}
